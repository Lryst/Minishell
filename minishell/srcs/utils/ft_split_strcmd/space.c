#include "minishell.h"

void	ft_count_word_space(char *s, int *i, int *n, int *echo)
{
	while (s[*i] && (s[*i] == ' ' || s[*i] == '\t'))
		(*i)++;
	if (*echo == 1)
		(*n)++;
}
