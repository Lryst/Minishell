#include "libft.h"

size_t			ft_strlen_no_space(char *str)
{
	size_t		i;

	i = 0;
	while (*str != '\0')
	{
		if (ft_isspace(*str))
			;
		else
			i++;
		str++;
	}
	return (i);
}
