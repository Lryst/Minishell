#include "libft.h"

void		ft_free_tab(char **tab)
{
	int		i;

	i = -1;
	if (tab)
	{
		while (tab[++i])
			ft_free(tab[i]);
		free(tab);
		tab = NULL;
	}
	tab = NULL;
}
